package com.example.korarmy;

public class Users {
    private String army;
    private String email;
    private String enlistment_date;
    private String username;
    private String discharge_date;

    public String getArmy() {
        return army;
    }

    public void setArmy(String army) {
        this.army = army;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getEnlistment_date() {
        return enlistment_date;
    }

    public void setEnlistment_date(String enlistment_date) {
        this.enlistment_date = enlistment_date;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getDischarge_date() {
        return discharge_date;
    }

    public void setDischarge_date(String discharge_date) {
        this.discharge_date = discharge_date;
    }
}
